package models;

import javax.persistence.*;
import java.util.*;

@Entity
public class Troop {

	@Id
	@GeneratedValue
	private Long id;

	public void setId(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	private Integer countSword;

	public void setCountSword(Integer countSword) {
		this.countSword = countSword;
	}

	public Integer getCountSword() {
		return countSword;
	}

	private Integer countAxe;

	public void setCountAxe(Integer countAxe) {
		this.countAxe = countAxe;
	}

	public Integer getCountAxe() {
		return countAxe;
	}

}
