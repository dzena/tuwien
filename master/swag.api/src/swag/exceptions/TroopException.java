package swag.exceptions;

public class TroopException extends Exception{

	private static final long serialVersionUID = -1845487877515686278L;
	
	public TroopException() {
		super();
	}
	public TroopException(String msg) {
		super(msg);
	}
	public TroopException(String msg,Throwable cause) {
		super(msg,cause);
	}

}
