package products;

import java.io.Serializable;

import products.parts.Arm;
import products.parts.Body;
import products.parts.Head;
import products.parts.Leg;
import products.parts.abstracts.Cap;

public class PartsContainer implements Serializable{

	private static final long serialVersionUID = 1663132135037646381L;
	
	private Head head;
	private Body body;
	private Leg leftLeg;
	private Leg rightLeg;
	private Arm leftArm;
	private Arm rightArm;
	private Cap cap;

	public Head getHead() {
		return head;
	}

	public void setHead(Head head) {
		this.head = head;
	}

	public Body getBody() {
		return body;
	}

	public void setBody(Body body) {
		this.body = body;
	}

	public Cap getCap() {
		return cap;
	}

	public void setCap(Cap cap) {
		this.cap = cap;
	}

	public Leg getLeftLeg() {
		return leftLeg;
	}

	public void setLeftLeg(Leg leftLegs) {
		this.leftLeg = leftLegs;
	}

	public Leg getRightLeg() {
		return rightLeg;
	}

	public void setRightLeg(Leg rightLegs) {
		this.rightLeg = rightLegs;
	}

	public Arm getLeftArm() {
		return leftArm;
	}

	public void setLeftArm(Arm leftArms) {
		this.leftArm = leftArms;
	}

	public Arm getRightArm() {
		return rightArm;
	}

	public void setRightArm(Arm rightArms) {
		this.rightArm = rightArms;
	}
	
	
}
