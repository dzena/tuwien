public class IDaoDeleteException extends Exception{

	private static final long serialVersionUID = 8041577551538125989L;
	
	public IDaoDeleteException() {
		super();
	}
	public IDaoDeleteException(String msg) {
		super(msg);
	}
	public IDaoDeleteException(String msg,Throwable cause) {
		super(msg,cause);
	}

}

