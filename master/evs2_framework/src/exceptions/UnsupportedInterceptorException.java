package exceptions;

public class UnsupportedInterceptorException extends Exception{

	private static final long serialVersionUID = -4775487877515686278L;
	
	public UnsupportedInterceptorException() {
		super();
	}
	public UnsupportedInterceptorException(String msg) {
		super(msg);
	}
	public UnsupportedInterceptorException(String msg,Throwable cause) {
		super(msg,cause);
	}

}
