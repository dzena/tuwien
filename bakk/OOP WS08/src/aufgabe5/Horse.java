package aufgabe5;


public class Horse extends Racer {
	int speed;
	long maxSpeedTime;
	
	public Horse(int speed, long maxSpeedTime) {
		this.speed = speed;
		this.maxSpeedTime = maxSpeedTime;
	}

	public int speed() {
		return this.speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public long getMaxSpeedTime() {
		return maxSpeedTime;
	}

	public void setMaxSpeedTime(long maxSpeedTime) {
		this.maxSpeedTime = maxSpeedTime;
	}
	public boolean faster(int indSpeed) {
		if(indSpeed < this.speed)
			return true;
		else
			return false;
	}
	
	
}
