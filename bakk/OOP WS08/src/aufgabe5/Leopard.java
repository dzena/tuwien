package aufgabe5;


public class Leopard extends Racer {
	int speed;
	int meter;
	
	public Leopard(int speed, int meter) {
		this.speed = speed;
		this.meter = meter;
	}

	public int getMeter() {
		return meter;
	}

	public void setMeter(int meter) {
		this.meter = meter;
	}

	public int speed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	public boolean faster(int indSpeed) {
		if(indSpeed < this.speed)
			return true;
		else
			return false;
	}
}
