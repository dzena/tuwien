package aufgabe8.flo;

/**
 * This class represents a Timber. It includes the given ammount of sticks
 *
 */
public class Timber implements LoggingInterface {
	private int numberOfSticksIncluded;
	
	public Timber(int capacity){
		numberOfSticksIncluded = capacity;
	}
	
	/**
	 * returns true if a Stick can be extracted from the timber and decreases the capacity
	 * of the timber by one. If the capacity is zero that false is returned.
	 * 
	 */
	public int getVolume() {
		return numberOfSticksIncluded;	
	}
	
	public void getStick(){
		numberOfSticksIncluded--;
	}
	
	public void write(boolean debug, String message) {
	}
	
	public void writeCompleteStatus() {
	}
}
